// Package sarvam implements the Sarvam AI provider.
//
// Sarvam AI (https://docs.sarvam.ai) is OpenAI wire-compatible for chat
// completions only. Its Text-to-Speech and Speech-to-Text APIs use their own
// native shapes (Sarvam's TTS returns a JSON body with base64-encoded audio,
// not raw binary; Sarvam's STT response uses different field names than
// OpenAI's transcription response) and are NOT delegated to the shared
// openai adapter — see speech.go / transcription.go for the hand-written
// conversions.
package sarvam

import (
	"context"
	"strings"
	"time"

	"github.com/maximhq/bifrost/core/providers/openai"
	providerUtils "github.com/maximhq/bifrost/core/providers/utils"
	schemas "github.com/maximhq/bifrost/core/schemas"
	"github.com/valyala/fasthttp"
)

// SarvamProvider implements the Provider interface for Sarvam AI's API.
type SarvamProvider struct {
	logger              schemas.Logger        // Logger for provider operations
	client              *fasthttp.Client      // HTTP client for unary API requests (ReadTimeout bounds overall response)
	streamingClient     *fasthttp.Client      // HTTP client for streaming API requests (no ReadTimeout; idle governed by NewIdleTimeoutReader)
	networkConfig       schemas.NetworkConfig // Network configuration including extra headers
	sendBackRawRequest  bool                  // Whether to include raw request in BifrostResponse
	sendBackRawResponse bool                  // Whether to include raw response in BifrostResponse
}

// NewSarvamProvider creates a new Sarvam AI provider instance.
func NewSarvamProvider(config *schemas.ProviderConfig, logger schemas.Logger) (*SarvamProvider, error) {
	config.CheckAndSetDefaults()

	requestTimeout := time.Second * time.Duration(config.NetworkConfig.DefaultRequestTimeoutInSeconds)
	client := &fasthttp.Client{
		ReadTimeout:         requestTimeout,
		WriteTimeout:        requestTimeout,
		MaxConnsPerHost:     config.NetworkConfig.MaxConnsPerHost,
		MaxIdleConnDuration: 30 * time.Second,
		MaxConnWaitTimeout:  requestTimeout,
		MaxConnDuration:     time.Second * time.Duration(schemas.DefaultMaxConnDurationInSeconds),
		ConnPoolStrategy:    fasthttp.FIFO,
	}

	client = providerUtils.ConfigureProxy(client, config.ProxyConfig, logger)
	client = providerUtils.ConfigureDialer(client, config.NetworkConfig.AllowPrivateNetwork)
	client = providerUtils.ConfigureTLS(client, config.NetworkConfig, logger)
	streamingClient := providerUtils.BuildStreamingClient(client)

	if config.NetworkConfig.BaseURL == "" {
		config.NetworkConfig.BaseURL = "https://api.sarvam.ai"
	}
	config.NetworkConfig.BaseURL = strings.TrimRight(config.NetworkConfig.BaseURL, "/")

	return &SarvamProvider{
		logger:              logger,
		client:              client,
		streamingClient:     streamingClient,
		networkConfig:       config.NetworkConfig,
		sendBackRawRequest:  config.SendBackRawRequest,
		sendBackRawResponse: config.SendBackRawResponse,
	}, nil
}

// GetProviderKey returns the provider identifier for Sarvam.
func (provider *SarvamProvider) GetProviderKey() schemas.ModelProvider {
	return schemas.Sarvam
}

// AuthHeaders returns Sarvam's auth headers for a key: the native
// api-subscription-key header (required by every Sarvam endpoint) plus
// Authorization: Bearer, which Sarvam's chat/completions endpoint also
// accepts for OpenAI-tooling compatibility.
func AuthHeaders(key schemas.Key) map[string]string {
	headers := openai.BearerAuthHeader(key)
	if key.Value.GetValue() != "" {
		headers["api-subscription-key"] = key.Value.GetValue()
	}
	return headers
}

// ListModels performs a list models request to Sarvam's API.
// GET /v1/models is undocumented in Sarvam's published OpenAPI spec, but is
// live and OpenAI-shaped (verified: {"object":"list","data":[{"id",...}]});
// it only enumerates the two chat models (sarvam-30b/sarvam-105b), not the
// separate speech/TTS/translation models (bulbul, saaras, saarika, ...),
// which have no discovery endpoint of their own.
func (provider *SarvamProvider) ListModels(ctx *schemas.BifrostContext, keys []schemas.Key, request *schemas.BifrostListModelsRequest) (*schemas.BifrostListModelsResponse, *schemas.BifrostError) {
	return openai.HandleOpenAIListModelsRequest(
		ctx,
		provider.client,
		request,
		provider.networkConfig.BaseURL+providerUtils.GetPathFromContext(ctx, "/v1/models"),
		keys,
		provider.networkConfig.ExtraHeaders,
		schemas.Sarvam,
		providerUtils.ShouldSendBackRawRequest(ctx, provider.sendBackRawRequest),
		providerUtils.ShouldSendBackRawResponse(ctx, provider.sendBackRawResponse),
	)
}

